//Atributo "parseInt", foi usado para converter a string em int.
console.log("10" / "2"); //O interpretador do JS entende que essa é uma divisão real entre números e converte automáticamente.
console.log("Ricardo" / 2); //Nesse caso o interpretador retorna (NaN = Not a Number).
console.log(7 / 2); //Neste caso o retorno será 3.5, pois o JS entende a virgula para separar os numeros flot como ponto flutuante, se escrevemos o mesmo valor com vírgula, é lido um espaço no lugar da vírgula, algo como 3 5.


